                    <p>Приемът в Софтуерния университет за онлайн и присъственото обучение се осъществява чрез приемен изпит по програмиране.</p>
                    <p>Приемен изпит ще се провежда на всеки два месеца. Студентите могат да изберат да се запишат за <strong>безплатния подготвителен курс</strong> (Ниво #0, който продължава 8 седмици) или направо да се явят на изпита, ако се чувстват достатъчно подготвени.</p>
                    <p>Изпитът е върху <strong>основи на програмирането</strong> и е с продължителност 6-8 часа. Той включва 5 практически задачи по програмиране. При решаването на всяка задача, студентът качва решението си в онлайн система за автоматизирана проверка и получава почти веднага своя резултат за съответната задача.</p>
                    <p>На приемния изпит може да програмирате на някой от следните езици за програмиране: <strong>C#</strong>, <strong>Java</strong>, <strong>JavaScript</strong>, <strong>C</strong>, <strong>C++</strong> (последни версии). Подготовката за приемния изпит ще бъде с езика C#. Използването на други езици за програмиране е на ваш собствен риск.</p>
                    <h2>Изпити</h2>
                    <p>Изпитите се провеждат присъствено и представляват практическо решаване на задачи или разработка и защита на практически проект.</p>
