                    <p>Явяването на изпит става само по покана от софтуерния университет. При явяване на изпити <strong>се изисква документ за самоличност</strong>. На изпитите е позволено да се ползва Интернет, както и предварително подготвени или публично достъпни в Интернет <strong>помощни материали</strong> (книги, учебници, презентации, видео уроци и други). На изпитите е забранено да се ползва чужда помощ под каквато и да е форма: разговори с колеги, ползване на e-mail, ползване на услуги за прехвърляне на файлове като Dropbox, писане във форуми, Facebook, Skype и използване на чат-програми. За някои изпити се изисква инсталиране и ползване на специализиран софтуер за превенция на непозволена комуникация (<strong>anti-cheat client</strong>), предоставен от софтуерния университет.</p>
                    <p>При установяване на <strong>преписване на изпит</strong> (ползване на ползване на чужда помощ на някои от изпитите или представяне на чудж проект като свой), се <strong>отнемат студентските права</strong> на заловения студент за период от 1 година. В този период студентът няма право да учи в софтуерния университет и не му се възстановяват таксите за обучение за текущото ниво.</p>
